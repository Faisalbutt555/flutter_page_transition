enum PageTransitionType {

  custom, // 自定义过渡效果

  fadeIn, // 渐显

  transferRight,  // 从右向左
  transferUp, // 从下向上

  slideInLeft, // 从左向右
  slideInRight, // 从右向左
  slideInUp, // 从下向上
  slideInDown, // 从上向下

  slideLeft, // 从左向右
  slideRight, // 从右向左
  slideUp, // 从下向上
  slideDown, // 从上向下

  slideParallaxLeft, // 从左向右
  slideParallaxRight, // 从右向左
  slideParallaxUp, // 从下向上
  slideParallaxDown, // 从上向下

  slideZoomLeft, // 从左向右
  slideZoomRight, // 从右向左
  slideZoomUp, // 从下向上
  slideZoomDown, // 从上向下

}